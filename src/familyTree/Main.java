    private void buildMonthsList(JComboBox monthsList) {

        monthsList.removeAllItems();
        for (int monthCount = 0; monthCount < 12; monthCount++)
            monthsList.addItem(monthCount+1);
    }
   
    private void buildDaysList(Calendar dateIn, JComboBox daysList, JComboBox monthsList) {

        daysList.removeAllItems();
        dateIn.set(Calendar.MONTH, monthsList.getSelectedIndex());
        int lastDay = bornday.getActualMaximum(Calendar.DAY_OF_MONTH);

        for (int dayCount = 1; dayCount <= lastDay; dayCount++)
            daysList.addItem(Integer.toString(dayCount));
    }
    
    
    
    
    
    private void loadData() {
        File file = new File("humanData.dat");
        if(!file.exists()){
            JOptionPane.showMessageDialog(this, "Khng tm thy file \"humanData.dat\"");
            return;
        }
        try {
            FileInputStream fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            humanList =  (Vector<Human>) ois.readObject();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Khng tm thy file \"humanData.dat\"");
        } catch (IOException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Xy ra li khi c file \"humanData.dat\"");
        }
    }
    
    private boolean saveData(){
        File file = new File("humanData.dat");
        try {
            ObjectOutputStream oos;
            try (FileOutputStream fos = new FileOutputStream(file)) {
                oos = new ObjectOutputStream(fos);
                oos.writeObject(humanList);
            }
            oos.close();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Xy ra li khi ghi file \"humanData.dat\"");
            return false;
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Xy ra li khi ghi file \"humanData.dat\"");
            return false;
        }
        return true;
    }
    
